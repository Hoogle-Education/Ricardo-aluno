public class App {
    public static void main(String[] args) {

        // cont recebe 20 (inicialização)
        // enquanto cont for maior que zero (verificação)
        // diminua cont de um em um (atualização)
        for(int cont = 20; cont >= 0; cont--){
            System.out.println("valor atual do cont = " + cont);
        }

        for (int i = 45; i >= 25; i = i-3) {
            System.out.println("Valor atual de i = " + i);
        }  

    }
}
